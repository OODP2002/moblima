/**
 * This represents an enum class which defines which category the Top 5 lists by
 */
public enum ListBy {
    /**
     * No particular category selected, both Sales and Average Rating Top 5 will be shown
     */
    NIL,
    /**
     * Only Top 5 Sales are shown
     */
    SALES,
    /**
     * Only Top 5 Average Ratings are shown
     */
    AVG_RATING
}
